using Gaming.Business;
using Gaming.Business.Mapping;
using Gaming.DataAccess.Data;
using Gaming.DataAccess.Repositories;
using Microsoft.EntityFrameworkCore;

var builder = WebApplication.CreateBuilder(args);

// Add services to the container.

builder.Services.AddControllers();
// Learn more about configuring Swagger/OpenAPI at https://aka.ms/aspnetcore/swashbuckle
builder.Services.AddEndpointsApiExplorer();
builder.Services.AddSwaggerGen();
builder.Services.AddScoped<IGameService, GameService>();
builder.Services.AddScoped<IGameRepository, EFGameRepository>();
builder.Services.AddAutoMapper(typeof(MapProfile));
builder.Services.AddDbContext<GameDbContext>(opt => opt.UseSqlServer(builder.Configuration.GetConnectionString("db")));



var app = builder.Build();

// Configure the HTTP request pipeline.
if (app.Environment.IsDevelopment())
{
    app.UseSwagger();
    app.UseSwaggerUI();
}

app.UseHttpsRedirection();

app.UseAuthorization();

app.MapControllers();

app.Run();
